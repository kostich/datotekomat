func generateFS(TotalSectors, BytesPerSector, TotalFSEntries int, Label, path string) (*Filesystem, error) {
	return New(TotalSectors, BytesPerSector, TotalFSEntries, Label, path, TestTimestamp)
}

func checkBinary(expected []byte, actual []byte) error {
	if len(expected) != len(actual) {
		return fmt.Errorf(
			"погрешна величина: очекивано %vБ, створено: %vБ",
			len(expected), len(actual),
		)
	}

	for i := range actual {
		if actual[i] != expected[i] {
			return fmt.Errorf(
				"неочекиван бајт на положају %v: очекивано 0x%X, уочено 0x%X\nочекивано: %X\nуочено: %X\n",
				i, expected[i], actual[i], expected, actual,
			)
		}
	}

	return nil
}

// verify bits
func runFSTests(t *testing.T, tests []fsTests) {
	t.Helper()

	for _, tt := range tests {
		// generate the temp directory and file
		tempDir := t.TempDir()
		tempFSFile, err := os.CreateTemp(tempDir, "testfs-*.img")
		if err != nil {
			t.Fatalf("%v: %v", tt.testname, err)
		}
		tempFSFile.Close()

		// generate the given fs
		var testfs *Filesystem
		_, err = generateFS(tt.totalSectors, tt.bytesPerSector, tt.totalFSEntries, tt.label, tempFSFile.Name())
		if err != nil {
			t.Fatalf("%v: %v", tt.testname, err)
		}

		// create folders (if specified)
		if len(tt.folders) > 0 {
			for _, folder := range tt.folders {
				testfs, err := Read(tempFSFile.Name())
				if err != nil {
					t.Fatalf("%v: %v", tt.testname, err)
				}

				// call the folder create function
				err = testfs.CreateFolder(folder.Path+folder.Name, TestTimestamp)
				if err != nil {
					t.Fatalf("%v: %v", tt.testname, err)
				}
			}
		}

		// copy the files to the given fs (if any)
		if len(tt.files) > 0 {
			for _, file := range tt.files {
				// write the file contents to a temp file
				tempTestFilePath := fmt.Sprintf("%v/%v", tempDir, file.Name)
				tempTestFile, err := os.Create(tempTestFilePath)
				if err != nil {
					t.Fatalf("%v: %v", tt.testname, err)
				}
				tempTestFile.Write(file.Contents)
				tempTestFile.Close()

				// set the correct permission
				err = os.Chmod(tempTestFilePath, fs.FileMode(file.Mode))
				if err != nil {
					t.Fatalf("%v: %v", tt.testname, err)
				}

				// call the copy function
				err = CopyFileIn(tempTestFile.Name(), file.Path, tempFSFile.Name(), TestTimestamp)
				if err != nil {
					t.Fatalf("%v: %v", tt.testname, err)
				}
			}
		}

		// rename the files (if specified)
		if len(tt.testRenames) > 0 {
			testfs, err = Read(tempFSFile.Name())
			if err != nil {
				t.Fatalf("%v: %v", tt.testname, err)
			}

			for k := range tt.testRenames {
				if err := testfs.RenameEntry(tt.testRenames[k].Current, tt.testRenames[k].New); err != nil {
					t.Fatalf("%v: %v", tt.testname, err)
				}
			}
		}

		// delete the files (if specified)
		if len(tt.testDeletes) > 0 {
			testfs, err = Read(tempFSFile.Name())
			if err != nil {
				t.Fatalf("%v: %v", tt.testname, err)
			}

			for k := range tt.testDeletes {
				if err := testfs.DeleteEntry(tt.testDeletes[k].Current); err != nil {
					t.Fatalf("%v: %v", tt.testname, err)
				}
			}

			// copy the files after delete (if specified)
			if len(tt.filesAfterDelete) > 0 {
				for _, file := range tt.filesAfterDelete {
					// write the file contents to a temp file
					tempTestFilePath := fmt.Sprintf("%v/%v", tempDir, file.Name)
					tempTestFile, err := os.Create(tempTestFilePath)
					if err != nil {
						t.Fatalf("%v: %v", tt.testname, err)
					}
					tempTestFile.Write(file.Contents)
					tempTestFile.Close()

					// set the correct permission
					err = os.Chmod(tempTestFilePath, fs.FileMode(file.Mode))
					if err != nil {
						t.Fatalf("%v: %v", tt.testname, err)
					}

					// call the copy function
					err = CopyFileIn(tempTestFile.Name(), file.Path, tempFSFile.Name(), TestTimestamp)
					if err != nil {
						t.Fatalf("%v: %v", tt.testname, err)
					}
				}
			}
		}

		// create links (if specified)
		if len(tt.links) > 0 {
			for _, link := range tt.links {
				testfs, err = Read(tempFSFile.Name())
				if err != nil {
					t.Fatalf("%v: %v", tt.testname, err)
				}

				// call the link create function
				err = testfs.CreateLink(link.Path, link.Name, TestTimestamp)
				if err != nil {
					t.Fatalf("%v: %v", tt.testname, err)
				}
			}
		}

		// change the entry permissions (if specified)
		if len(tt.modeChange) > 0 {
			testfs, err = Read(tempFSFile.Name())
			if err != nil {
				t.Fatalf("%v: %v", tt.testname, err)
			}

			for _, file := range tt.modeChange {
				err = testfs.ChangeEntryMode(file.Path+file.Name, fmt.Sprint(file.Mode))
				if err != nil {
					t.Fatalf("%v: %v", tt.testname, err)
				}
			}
		}

		// change the entry uid/gid (if specified)
		if len(tt.idChange) > 0 {
			testfs, err = Read(tempFSFile.Name())
			if err != nil {
				t.Fatalf("%v: %v", tt.testname, err)
			}

			for _, file := range tt.idChange {
				err = testfs.ChangeUIDGID(file.Path+file.Name, fmt.Sprintf("%v:%v", file.UID, file.GID))
				if err != nil {
					t.Fatalf("%v: %v", tt.testname, err)
				}
			}
		}

		// change the entry timestamps (if specified)
		if len(tt.timestampChange) > 0 {
			testfs, err = Read(tempFSFile.Name())
			if err != nil {
				t.Fatalf("%v: %v", tt.testname, err)
			}

			for _, item := range tt.timestampChange {
				err = testfs.ChangeTimestamp(item.Name, item.TimestampWhich, item.TimestampTime)
				if err != nil {
					t.Fatalf("%v: %v", tt.testname, err)
				}
			}
		}

		// check if the generated fs contains all of the expected files
		if len(tt.expectedEntries) > 0 {
			testfs, err = Read(tempFSFile.Name())
			if err != nil {
				t.Fatalf("%v: %v", tt.testname, err)
			}

			fsFiles := []string{}
			rootFolder := make([]byte, FILENAME_LENGTH)
			copy(rootFolder, []byte{byte('/')})
			for i := 0; i < int(testfs.SuperBlock.TotalFSEntries); i++ {
				entry, err := testfs.FSEntries.GetEntry(testfs.Path, testfs.SuperBlock, i)
				if err != nil {
					t.Fatalf("%v: %v", tt.testname, err)
				}

				if !entryDeleted(string(entry.Name)) &&
					string(entry.Name) != string(rootFolder) &&
					string(bytes.TrimRight(entry.Name, "\x00")) != "" {
					fsFiles = append(fsFiles, string(entry.Name))
				}
			}

			if len(fsFiles) != len(tt.expectedEntries) {
				t.Fatalf("%v: број очекиваних ставки: %v, број уочених: %v, уочене: %+v", tt.testname, len(tt.expectedEntries), len(fsFiles), fsFiles)
			}

			for k, expectedFile := range tt.expectedEntries {
				fname := make([]byte, FILENAME_LENGTH)
				copy(fname, []byte(expectedFile))

				if fsFiles[k] != string(fname) {
					t.Fatalf("%v: очекивана ставка: %v, добијена: %v", tt.testname, string(fname), fsFiles[k])
				}
			}
		}

		// check if the copying out functionality works
		if len(tt.testCopyOut) > 0 {
			for k := range tt.testCopyOut {
				// copy out the file
				tempCopyOutFile, err := os.CreateTemp(tempDir, "testfsfile-*.txt")
				if err != nil {
					t.Fatalf("%v: %v", tt.testname, err)
				}
				tempCopyOutFile.Close()

				err = CopyFileOut(tt.testCopyOut[k].Current, tempCopyOutFile.Name(), tempFSFile.Name())
				if err != nil {
					t.Fatalf("%v: %v", tt.testname, err)
				}

				// read the written file and compare the contents
				fileBinary, err := os.ReadFile(tempCopyOutFile.Name())
				if err != nil {
					t.Fatalf("%v: %v", tt.testname, err)
				}

				err = checkBinary(tt.files[k].Contents, fileBinary)
				if err != nil {
					t.Fatalf("%v: %v", tt.testname, err)
				}
			}
		}

		// re-read the fs from the disk
		fsBinary, err := os.ReadFile(tempFSFile.Name())
		if err != nil {
			t.Fatalf("%v: %v", tt.testname, err)
		}

		// check if the generated fs matches the expected binary
		err = checkBinary(tt.expectedBinary, fsBinary)
		if err != nil {
			t.Fatalf("%v: %v", tt.testname, err)
		}
	}
}

